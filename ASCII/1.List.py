# conccept [ list is a collection of items in a defined order.You can change,add,andremove items.]

# example

fruits = ['apple', 'banana', 'cherry']
print(fruits[0])  # Access
print(fruits[1])  # Access
fruits.remove("banana")    # Remove
fruits.append("banana")    # add
fruits[1] = "grapes"       # Update

print(fruits)



